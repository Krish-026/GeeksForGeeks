using namespace std;
int rightMost(int m, int n)
{
    int x = m ^ n; // to find different bits
    return log2(x & (~(x - 1))) + 1;
}
void solve()
{
    int m, n;
    cin >> m >> n;
    int ans = rightMost(m, n);
    cout << ans;
}
